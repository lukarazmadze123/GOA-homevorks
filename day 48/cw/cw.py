import random

# ფუნქცია რომელიც შემთხვევით ფილმს ირჩევს
def random_movie():
    # სია რომელშიც ჩვენს საყვარელ ფილმებს ვათავსებთ
    list = ["interstellar", "inception", "მრავალვარსკვლავიანი", "მეგობარი და ლარი", "ზვიადი ქროლი"]
    
    # სია რომელიც შეიცავს შემთხვევითი ფილმების არჩევისათვის საჭირო მონაცემებს
    list_of_random_movies = ["მზიური", "შეფეს", "დამს", "მამხზი", "პატარა ბიჭი", "ღამის მომავალი"]

    # ციკლი რომელიც 4-ჯერ გამეორდება
    for i in range(4):
        # შემთხვევითი ფილმის არჩევა
        random_item = random.choice(list_of_random_movies)
        
        # შემთხვევითი ფილმის დამატება სიაში
        list.append(random_item)
        
        # ფილმის ამოღება
        list_of_random_movies.remove(random_item)

    # საბოლოო სიას დავბეჭდავთ
    return list

